function isClearlyOutOfScope(message, locale) {
  const normalized = normalizeMessage(message);
  const outOfScopeTerms =
    locale === 'EN'
      ? [
          'weather',
          'temperature',
          'football score',
          'stock market',
          'bitcoin',
          'recipe',
          'movie review',
          'politics',
          'news',
          'horoscope'
        ]
      : [
          'temperatura',
          'resultado do jogo',
          'bolsa de valores',
          'bitcoin',
          'receita',
          'filme',
          'politica',
          'noticias',
          'horoscopo'
        ];

  return outOfScopeTerms.some((term) => normalized.includes(term));
}

