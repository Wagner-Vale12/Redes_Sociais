function extractKeywords(message, locale) {
  const stopwords =
    locale === 'EN'
      ? new Set(['the', 'and', 'for', 'with', 'about', 'what', 'does', 'from', 'that', 'this', 'have', 'work', 'tell'])
      : new Set(['que', 'com', 'para', 'sobre', 'qual', 'quais', 'como', 'onde', 'ele', 'ela', 'tem', 'dos', 'das']);

  return normalizeMessage(message)
    .split(/[^a-z0-9]+/)
    .filter((word) => word.length > 2 && !stopwords.has(word));
}

function searchPortfolio(message, data, locale) {
  const keywords = extractKeywords(message, locale);

  if (!keywords.length) {
    return '';
  }

  const entries = buildKnowledgeEntries(data, locale)
    .map((entry) => {
      const haystack = normalizeMessage(`${entry.title} ${entry.text}`);
      let score = 0;

      keywords.forEach((keyword) => {
        if (haystack.includes(keyword)) {
          score += entry.title.toLowerCase().includes(keyword) ? 3 : 1;
        }
      });

      return { ...entry, score };
    })
    .filter((entry) => entry.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 3);

  if (!entries.length) {
    return '';
  }

  return formatChatResponse(
    getResponseLead(locale, 'search'),
    [
      locale === 'EN'
        ? 'I matched your question with these portfolio sections:'
        : 'Relacionei sua pergunta com estas partes do portfólio:',
      ...entries.map((entry) => `${entry.title}\n${entry.text}`)
    ]
  );
}

